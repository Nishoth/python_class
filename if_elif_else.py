# if ,elif else
age = 20
if age > 20:
    print("you can vote")
elif age == 20:
    print("you can apply for voter ID")
# use the elif condition for multy condition
else:
    print("you can't vote")

# And
a , b = 10 , 20
if a == 10 and b == 20:
    print("condition satisfied")
else:
    print("condition not satisfied")

# Or
a , b = 10 , 20
if a == 10 or b == 5:
    print("condition satisfied")
else:
    print("condition not satisfied")

# nested if
a , b = 10 , 20
if a == 10 :
    print("condition satisfied")
    if b == 20:
        print("condition completely satisfied")
else:
    print("condition not satisfied")

    



